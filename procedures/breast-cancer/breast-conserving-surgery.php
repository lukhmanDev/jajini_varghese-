                        <h2>What Is Breast Conserving Surgery?</h2>
                        <p>Breast conserving surgery involves removing the cancer along with a margin of healthy surrounding tissue, while keeping the rest of the breast intact. In most cases, this surgery is followed by radiotherapy, which significantly reduces the risk of cancer returning in the breast. For carefully selected patients, long-term outcomes are equivalent to those of mastectomy.</p>
                        <p>The guiding principle is always complete and safe cancer removal, combined with careful planning to preserve breast shape wherever possible. This balance between effective cancer treatment and appearance is central to Ms Varghese’s approach.</p>
