                        <h2>Types of Mastectomy</h2>
                        <p>The type of mastectomy recommended depends on the breast cancer diagnosis, breast shape, and personal circumstances. Options may include a simple (total) mastectomy, which removes the breast tissue and skin to create a flat contour; a skin-sparing mastectomy, where most of the breast skin is preserved and reconstruction is performed; or a nipple-sparing mastectomy, where the nipple and skin can be safely preserved in selected cases.</p>
                        <p>Where oncologically appropriate, skin- or nipple-sparing mastectomy techniques can provide a more natural foundation for breast reconstruction, and Ms Varghese will carefully explain whether these options are suitable, based on both safety and long-term outcomes.</p>
