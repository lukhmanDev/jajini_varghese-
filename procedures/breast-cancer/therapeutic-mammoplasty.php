                        <h2>Who May Benefit from Therapeutic Mammoplasty?</h2>
                        <p>Therapeutic mammoplasty may be an excellent option for women with breast cancer who have
                            medium to large breasts and are concerned about breast shape following lumpectomy. It can
                            also be suitable for women who have been advised to consider mastectomy due to breast size
                            or tumour position, or who already experience discomfort related to heavy breasts.</p>
                        <p>Where appropriate, Ms Varghese can also perform symmetrising surgery on the opposite breast
                            to achieve balance. Axillary surgery, when required, may be performed at the same time and
                            can occasionally involve a separate scar, which is discussed clearly in advance.</p>
