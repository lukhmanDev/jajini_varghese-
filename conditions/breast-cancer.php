                        <h2>Breast Cancer</h2>
                        <p>The most common cancer in the UK, breast cancer mainly, though not exclusively, affects women. Survival rates are increasing, and we know that early diagnosis plays a crucial role in better treatment outcomes. That means checking your breasts regularly is key.</p>
                        <p>While breast cancer can affect younger women, the risk of getting breast cancer increases as we get older, and the majority of patients are women aged over 50. While there is a genetic link in some cases, the majority of women with breast cancer have no family history of the disease.</p>
                        <h2>Are there different types of breast cancer?</h2>
                        <p>Breast cancer is not a single disease. There are several different types of breast cancer, each behaving differently and requiring an individualised approach to diagnosis and treatment. Advances in screening, imaging, genetics, and treatment mean that breast cancer is increasingly detected earlier and treated more effectively, with excellent outcomes for many patients.</p>
                        <p>Care is delivered through a specialist multidisciplinary team (MDT), where every case is discussed in detail. This team brings together breast surgeons, plastic and reconstructive surgeons, oncologists, radiologists, pathologists, geneticists, specialist nurses, and research teams. Working closely together ensures that each patient benefits from expert opinion across all aspects of care, with treatment plans tailored to the individual.</p>
                        <p>Our MDT is research-intensive, actively involved in national and international clinical trials and the translation of new scientific discoveries into clinical practice. This means patients may have access to the latest evidence-based treatments, innovative surgical techniques, and personalised therapies, where appropriate.</p>
                        <p>The most common types of breast cancer include:</p>
                        <ul>
                            <li><b>Invasive breast cancer:</b> The most common form of breast cancer, occurring when cancer cells spread beyond the milk ducts into surrounding breast tissue. While not always aggressive, it has the potential to spread and therefore requires timely, specialist treatment.</li>
                            <li><b>Ductal carcinoma in situ (DCIS):</b> An early, non-invasive form of breast cancer where abnormal cells are confined to the milk ducts. Although not invasive, treatment is recommended to reduce the risk of future invasive disease.</li>
                            <li><b>Invasive lobular breast cancer:</b> This type begins in the lobules, the glands that produce breast milk, before spreading into nearby breast tissue. It can be more subtle on imaging and benefits from expert assessment and tailored surgical planning.</li>
                             <li><b>Triple negative breast cancer:</b> A less common but more aggressive type of breast cancer. It lacks oestrogen, progesterone, and HER2 receptors, which means some standard targeted treatments are not effective. However, advances in research have led to newer treatment options that have significantly improved outcomes for many patients.</li>
                             <li><b>Inflammatory breast cancer:</b> A rare and aggressive form that spreads through the lymph vessels in the skin of the breast. It often presents with redness, swelling, and skin changes rather than a distinct lump and requires prompt specialist assessment.</li>
                             <li><b>Angiosarcoma of the breast: </b>A very rare cancer arising from the soft tissues of the breast. It can grow and spread quickly and is managed within a highly specialised, multidisciplinary setting.</li>
                             <li><b>Male breast cancer: </b>Breast cancer can also affect men, developing in the small amount of breast tissue behind the nipple. Although rare and more common in men over 60, it can occur at any age and should always be assessed promptly.</li>
                        </ul>
                        <p>Every breast cancer diagnosis is unique. Through collaborative MDT working, research-led care, and personalised treatment planning, our approach is focused on achieving the best possible clinical outcomes while supporting patients as individuals throughout their treatment journey.</p>
                        
