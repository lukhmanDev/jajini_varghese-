                        <h2>What is a capsular contracture?</h2>
                        <p>Capsular contracture occurs when the scar tissue (capsule) that naturally forms around a breast implant tightens and hardens. This can lead to firmness, distortion of breast shape, discomfort, and in some cases pain. Capsular contracture is a recognised complication of both breast augmentation and breast reconstruction, occurring in approximately 10–15% of cases.</p>
                        <p>Several factors are known to increase the risk of developing capsular contracture, including:</p>
                        <ul>
                            <li>Previous breast surgery or implant revision</li>
                            <li>Infection or inflammation around the implant</li>
                            <li>Bleeding or fluid collection (haematoma or seroma) after surgery</li>
                            <li>Radiotherapy to the breast following cancer treatment</li>
                            <li>Implant placement above the muscle</li>
                            <li>Certain implant types or surface characteristics</li>
                            <li>Smoking, which affects healing</li>
                            <li>A history of capsular contracture</li>
                        </ul>
                        <p>While preventative measures — such as careful surgical technique, appropriate implant placement, and post-operative massage — can help reduce risk, capsular contracture can still occur.</p>
                        <p>When symptoms are significant, treatment may involve surgery to remove or release the tightened capsule (capsulectomy or capsulotomy), often combined with implant replacement to restore comfort and breast shape.</p>
